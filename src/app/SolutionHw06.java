package app;

public class SolutionHw06 {
    public static void main(String[] args) {
        int[] source = new int[]{1, 2, 3, 4, 5, 6};
        int sum = 0;
        for (int i : source) {
            sum += i;
            System.out.printf("%d Num is %d, sum is %d %n",
                    i, i, sum);
        }
        System.out.printf("Sum of numbers is %d", sum);











    }








}
